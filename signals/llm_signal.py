if __name__ == "__main__":
    tests = [
        ("AI test", "Artificial intelligence represents a transformative paradigm shift in modern society. It is important to note that while the benefits are numerous, stakeholders must collaborate to ensure responsible deployment."),
        ("Human test", "ok so i finally tried that new ramen place downtown and honestly? underwhelming. the broth was fine but they put WAY too much sodium in it and i was thirsty for like three hours after."),
    ]
    for label, text in tests:
        result = groq_score(text)
        print(f"\n[{label}]")
        print(f"  Score: {result['score']}")
        print(f"  Reasoning: {result['reasoning']}")